#include <unistd.h>

void	ft_print_comb(void)
{
	int	i;
	int	j;
	int	r;

	i = '0';
	while (i < '8')
	{
		j = i + 1;
		while (j < '9')
		{
			r = j + 1;
			while (r <= '9')
			{
				write(1, &i, 1);
				write(1, &j, 1);
				write(1, &r, 1);
				if (!(i == '7' && r == '9'))
					write(1, ", ", 2);
				r++;
			}
			j++;
		}
		i++;
	}
}

